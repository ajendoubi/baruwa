CREATE TABLE archive (
    id varchar(255) NOT NULL PRIMARY KEY,
    actions text NOT NULL,
    clientip char(15) NOT NULL,
    'date' date NOT NULL,
    from_address text NOT NULL,
    from_domain text NOT NULL,
    headers text NOT NULL,
    hostname text NOT NULL,
    highspam integer NOT NULL,
    rblspam integer NOT NULL,
    saspam integer NOT NULL,
    spam integer NOT NULL,
    nameinfected integer NOT NULL,
    otherinfected integer NOT NULL,
    isquarantined integer NOT NULL,
    sascore real NOT NULL,
    scaned integer NOT NULL,
    size integer NOT NULL,
    blacklisted integer NOT NULL,
    spamreport text NOT NULL,
    whitelisted integer NOT NULL,
    subject text NOT NULL,
    time time NOT NULL,
    timestamp datetime NOT NULL,
    to_address text NOT NULL,
    to_domain text NOT NULL,
    virusinfected integer NOT NULL
);
